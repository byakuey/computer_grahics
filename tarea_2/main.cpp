void reshape ( int width , int height )
{
	if (height==0) // Prevenir division entre cero
		height=1;

	glViewport(0, 0, width,height);

	glMatrixMode(GL_PROJECTION); // Seleccionamos Projection Matrix
	glLoadIdentity();

	// Ortogonal
	glOrtho(0, 600, 600, 0, -2, 2);

	glMatrixMode(GL_MODELVIEW); // Seleccionamos Modelview Matrix
	glLoadIdentity();
}

// Para cerrar la ventana con la tecla ESC
void keyboard(unsigned char key, int x, int y)
{
	 switch (key)
	 {
		case 27:
            exit(0);
			break;
	 }
	  glutPostRedisplay();
}

// Para cambiar de dibujo con las flechas
void arrow_key(int key, int x, int y)
{
	 switch (key)
	 {
        case GLUT_KEY_RIGHT:
            cambio++;
            cambio = (cambio > 3) ? 0 : cambio;
            break;
        case GLUT_KEY_LEFT:
            cambio--;
            cambio = (cambio < 0) ? 3 : cambio;
            break;
	 }
	  glutPostRedisplay();
}

int main (int argc, char** argv)
{
  glutInit            (&argc, argv); // Inicializamos OpenGL
  glutInitDisplayMode (GLUT_RGBA | GLUT_SINGLE); // Display Mode (Clores RGB y alpha | Buffer Sencillo )
  glutInitWindowSize  (700, 700); // Tamaño de la Ventana
  glutInitWindowPosition (-1, -1); //Posicion de la Ventana
  glutCreateWindow    ("Polygon Art"); // Nombre de la Ventana
  InitGL(); // Parametros iniciales de la aplicacion
  glutDisplayFunc(display); // Función de dibujo
  glutReshapeFunc(reshape); // Función en caso de cambio de tamano
  glutKeyboardFunc(keyboard); // Función de manejo de teclado
  glutSpecialFunc(arrow_key); // Función manejo teclas especiales
  glutMainLoop();

  return 0;
}
